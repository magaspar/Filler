#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int j;
	int k;

	i = 0;
	j = 0;
	k = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
	{
		i++;
	}
	if (str[i] == '-')
		j = 1;
	if ((str[i] == '-') || (str[i] == '+'))
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		k = k * 10;
		k = k + str[i] - '0';
		i++;
	}
	if (j)
		return (-k);
	else
		return (k);
}
